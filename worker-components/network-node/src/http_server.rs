// ── User-facing handler (POST /) ─────────────────────────────────────────────

/// Shared state for the user-facing request handler. The secrets provider
/// abstracts over monolith/maintainer (in-process) vs handler (remote-cached);
/// `pke_dk_bytes` is loaded directly from CLI on both maintainer and handler,
/// so it never crosses the wire.
#[derive(Clone)]
pub struct AppState {
    pub provider: Arc<SecretsProvider>,
    pub chain_rpc: Arc<ChainRpcConfig>,
    pub concurrency: Arc<Semaphore>,
    pub pke_dk_bytes: Arc<Vec<u8>>,
}

/// Spawn the user-request server. Runs until the process exits.
///
/// CORS is wide open (`Access-Control-Allow-Origin: *`, any method, any header)
/// so browser-based clients (e.g. the Shelby explorer's decrypt flow at
/// `https://explorer.shelby.xyz`, or any dApp using `@aptos-labs/ace-sdk`
/// directly from the page) can read the share response. The endpoint has no
/// cookies, no Authorization, and no per-origin authorization — the response
/// ciphertext is only readable by the holder of the ephemeral PKE key encoded
/// in the request — so there is nothing for the SOP to protect here.
pub async fn run_user_server(port: u16, state: AppState) {
    let app = Router::new()
        .route("/", post(handle_request))
        .route("/healthz", get(handle_healthz))
        .with_state(state)
        .layer(CorsLayer::permissive());
    serve(port, app, "http-server (user)").await;
}

async fn handle_healthz() -> StatusCode {
    StatusCode::OK
}

// ── Secrets-server (GET /secrets, maintainer mode) ───────────────────────────
//
// No app-level auth: maintainer and handler are assumed to live in the same
// private network (Cloud Run `ingress=internal` + IAM run.invoker, or a
// VPC-scoped service).

#[derive(Clone)]
pub struct SecretsServerState {
    pub local: LocalSecrets,
}

pub async fn run_secrets_server(port: u16, state: SecretsServerState) {
    let app = Router::new()
        .route("/secrets", get(handle_get_secrets))
        .route("/healthz", get(handle_secrets_healthz))
        .with_state(state);
    serve(port, app, "http-server (secrets)").await;
}

async fn handle_get_secrets(State(state): State<SecretsServerState>) -> Json<SecretsSnapshotWire> {
    Json(state.local.snapshot_wire().await)
}

async fn handle_secrets_healthz() -> StatusCode {
    StatusCode::OK
}

// ── Common serve helper ──────────────────────────────────────────────────────

async fn serve(port: u16, app: Router, label: &str) {
    let addr = format!("0.0.0.0:{}", port);
    let listener = match tokio::net::TcpListener::bind(&addr).await {
        Ok(l) => l,
        Err(e) => {
            wlog!("{}: bind {} failed: {}", label, addr, e);
            return;
        }
    };
    wlog!("{}: listening on {}", label, addr);
    if let Err(e) = axum::serve(listener, app).await {
        wlog!("{}: serve error: {}", label, e);
    }
}

