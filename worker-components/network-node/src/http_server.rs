//! HTTP server that handles `POST /` requests for threshold-IBE partial key extraction.
//!
//! Request body:  hex-encoded PKE ciphertext (encrypted to this node's registered key) whose
//!                plaintext is a BCS [`verify::WorkerRequest`].
//! Response body: hex-encoded PKE ciphertext (encrypted to the client's key) whose
//!                plaintext is a BCS `tibe.IdentityDecryptionKeyShare` for
//!                decryption flows, or a BCS `ThresholdVrfShare` for tVRF.
//!
//! Adding a new request flow or chain is a one-arm change in `verify::WorkerRequest`
//! and a matching arm here — no manual byte-walking, no per-scheme size tables.
//!
//! ## Per-request structured log line
//!
//! Every request emits **one** JSON-formatted log line (to stderr) tagged with
//! `kind=ACE_REQUEST_HANDLING_SUMMARY`. Fields:
//!
//!   ts                  — UTC timestamp the response was produced
//!   kind                — always `"ACE_REQUEST_HANDLING_SUMMARY"` (search keyword)
//!   handling_session_id — UUID v4, generated by this worker when it begins handling
//!                         the request. Server-side identity; no relationship to any
//!                         client-side identifier.
//!   flow                — `"basic"`, `"custom"`, `"threshold_vrf"`, or `"?"` (when not yet identifiable)
//!   result              — `"ok"` | `"rejected"` | `"error"`
//!   reason              — short label for non-ok outcomes
//!   elapsed_ms          — wall time from request entry to response
//!   detail              — free-text reason detail, optional
//!   share_bytes         — only on `result=ok`
//!   keypair             — first 8 hex chars of keypair_id, once known
//!   epoch               — once the request is parsed
//!   decrypt_ms          — time spent PKE-decrypting the inbound request body
//!   pfn_ms              — time spent in `verify` (sole source of PFN RPC traffic)
//!   extract_ms          — time spent extracting the share + PKE-encrypting the response
//!
//! Together `decrypt_ms + pfn_ms + extract_ms` ≈ `elapsed_ms`; comparing the
//! three components is the cheapest way to attribute per-request latency.
//!
//! ## `GET /secrets` (maintainer mode only)
//!
//! Returns the current snapshot of secret shares + PKE dk + eval-point as JSON
//! (see [`crate::secrets::SecretsSnapshotWire`]). Optionally guarded by a
//! bearer token (config flag `--secrets-auth-token` on the maintainer side and
//! `--s0-auth-token` on the handler side).

